target = "AQI"

def predict_aqi(input_datetime):

    input_datetime = pd.to_datetime(input_datetime)

    # Get last 24 rows before that datetime
    past_data = df[df["datetime"] < input_datetime].tail(24)

    if len(past_data) < 24:
        print("Not enough historical data")
        return None

    past_values = past_data[features + [target]].values
    past_scaled = scaler.transform(past_values)

    X_input = past_scaled[:, :-1]
    X_input = X_input.reshape(1, 24, len(features))

    pred_scaled = model.predict(X_input)

    # Reconstruct full row to inverse scale
    temp = np.zeros((1, len(features) + 1))
    temp[0, -1] = pred_scaled

    pred_actual = scaler.inverse_transform(temp)[0, -1]

    return pred_actual

